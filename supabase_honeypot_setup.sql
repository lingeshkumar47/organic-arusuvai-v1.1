      secure_admin_id, '00000000-0000-0000-0000-000000000000', 'authenticated', 'authenticated', 
      'masteradmin@oa.online', 
      '{"provider":"email","providers":["email"]}', '{"full_name": "Master Admin"}', 
      false, crypt('masteradmin123', gen_salt('bf')), now(), now(), now()
    );
  ELSE
    SELECT id INTO secure_admin_id FROM auth.users WHERE email = 'masteradmin@oa.online';
  END IF;

  INSERT INTO public.profiles (id, full_name, role)
  VALUES (secure_admin_id, 'Master Admin', 'admin')
  ON CONFLICT (id) DO UPDATE SET role = 'admin';
END $$;

