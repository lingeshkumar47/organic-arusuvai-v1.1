-- 4. Update trigger to auto-assign admin for known emails
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
DECLARE
  assigned_role TEXT := 'customer';
BEGIN
  IF (new.email IN ('organic.arusuvai@gmail.com', 'masteradmin@oa.online', 'masteradmin@example.com')) THEN
    assigned_role := 'admin';
  END IF;
  INSERT INTO public.profiles (id, full_name, avatar_url, role)
  VALUES (new.id, new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'avatar_url', assigned_role)
  ON CONFLICT (id) DO UPDATE SET role = EXCLUDED.role;
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


