-- 1. Create phishing_attempts table to log honeypot entries
CREATE TABLE IF NOT EXISTS public.phishing_attempts (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  entered_email TEXT,
  entered_password TEXT,
  ip_address TEXT,
  user_agent TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

ALTER TABLE public.phishing_attempts ENABLE ROW LEVEL SECURITY;

-- Only admins can read phishing attempts
CREATE POLICY "Admins can view phishing attempts" ON public.phishing_attempts
FOR SELECT TO authenticated
USING ((SELECT role FROM public.profiles WHERE id = auth.uid()) = 'admin');

-- Anyone can insert (the honeypot needs to work for unauthenticated users too)
CREATE POLICY "Anyone can insert phishing attempts" ON public.phishing_attempts
FOR INSERT WITH CHECK (true);

